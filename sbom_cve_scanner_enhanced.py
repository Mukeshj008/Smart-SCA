# SBOM PARSER
# =============================================================================
# Parses CycloneDX Software Bill of Materials (SBOM) files to extract
# component information. Supports both JSON and XML formats.
# =============================================================================

class CycloneDXParser:
    """
    Parser for CycloneDX SBOM files (JSON and XML formats).
    
    CycloneDX is a lightweight SBOM standard designed for security contexts.
    This parser extracts component information including:
    - Component name and version
    - Package URL (purl) for ecosystem identification
    - Group ID (for Maven packages)
    
    Supported formats:
    - JSON (.json)
    - XML (.xml) with CycloneDX namespace
    
    Example usage:
        parser = CycloneDXParser()
        components = parser.parse("sbom.json")
        for comp in components:
            print(f"{comp.name}@{comp.version}")
    """
    
    CYCLONEDX_NS = {
        '1.4': 'http://cyclonedx.org/schema/bom/1.4',
        '1.5': 'http://cyclonedx.org/schema/bom/1.5',
        '1.6': 'http://cyclonedx.org/schema/bom/1.6',
    }
    
    def parse(self, file_path: str) -> list[Component]:
        """Parse a CycloneDX SBOM file and return components."""
        path = Path(file_path)
        content = path.read_text()
        
        if path.suffix.lower() == '.json':
            return self._parse_json(content)
        elif path.suffix.lower() == '.xml':
            return self._parse_xml(content)
        else:
            try:
                return self._parse_json(content)
            except json.JSONDecodeError:
                return self._parse_xml(content)
    
    def _parse_json(self, content: str) -> list[Component]:
        """Parse CycloneDX JSON format."""
        data = json.loads(content)
        components = []
        
        for comp in data.get('components', []):
            name = comp.get('name', '')
            version = comp.get('version', '')
            purl = comp.get('purl', '')
            comp_type = comp.get('type', 'library')
            group = comp.get('group', '')
            
            ecosystem = self._extract_ecosystem(purl)
            
            components.append(Component(
                name=name,
                version=version,
                purl=purl,
                component_type=comp_type,
                ecosystem=ecosystem,
                group_id=group
            ))
        
        return components
    
    def _parse_xml(self, content: str) -> list[Component]:
        """Parse CycloneDX XML format."""
        root = ET.fromstring(content)
        
        ns = ''
        for version, namespace in self.CYCLONEDX_NS.items():
            if namespace in root.tag:
                ns = f'{{{namespace}}}'
                break
        
        components = []
        for comp in root.findall(f'.//{ns}component'):
            name_elem = comp.find(f'{ns}name')
            version_elem = comp.find(f'{ns}version')
            purl_elem = comp.find(f'{ns}purl')
            group_elem = comp.find(f'{ns}group')
            
            name = name_elem.text if name_elem is not None else ''
            version = version_elem.text if version_elem is not None else ''
            purl = purl_elem.text if purl_elem is not None else ''
            group = group_elem.text if group_elem is not None else ''
            comp_type = comp.get('type', 'library')
            
            ecosystem = self._extract_ecosystem(purl)
            
            components.append(Component(
                name=name,
                version=version,
                purl=purl,
                component_type=comp_type,
                ecosystem=ecosystem,
                group_id=group
            ))
        
        return components
    
    def _extract_ecosystem(self, purl: str) -> str:
        """Extract ecosystem from Package URL."""
        if not purl:
            return "unknown"
        
        ecosystem_map = {
            'pkg:npm': 'npm',
            'pkg:pypi': 'PyPI',
            'pkg:maven': 'Maven',
            'pkg:nuget': 'NuGet',
            'pkg:gem': 'RubyGems',
            'pkg:golang': 'Go',
            'pkg:cargo': 'crates.io',
            'pkg:composer': 'Packagist',
            'pkg:deb': 'Debian',
            'pkg:rpm': 'RPM',
            'pkg:apk': 'Alpine',
        }
        
        for prefix, ecosystem in ecosystem_map.items():
            if purl.startswith(prefix):
                return ecosystem
        
        return "unknown"


