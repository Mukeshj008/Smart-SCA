                    conditions=analysis_data.get('conditions', []),
                    conditions_met=analysis_data.get('conditions_met', []),
                    conditions_not_met=analysis_data.get('conditions_not_met', []),
                    exploitability=analysis_data.get('exploitability', 'UNKNOWN'),
