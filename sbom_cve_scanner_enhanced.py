class HeuristicAnalyzer(AIProvider):
    """
    Heuristic-based exploit condition analyzer - THE DEFAULT ANALYZER.
    
    *** NO API KEY REQUIRED ***
    
    This analyzer uses pattern matching and predefined rules to assess exploitability.
    Optionally enhanced with Exploit-DB intelligence (use --exploit-db flag).
    
    ANALYSIS SOURCES:
    
    1. Pattern Matching (Heuristic):
       - CVE description keyword analysis
       - Vulnerability type detection (deserialization, SQLi, RCE, etc.)
       - Code context pattern matching
    
    2. Exploit-DB Intelligence (OPTIONAL - use --exploit-db):
       - Public exploits from exploit-db.com
       - Verified exploits and PoCs
       - Metasploit modules
    
    SUPPORTED VULNERABILITY PATTERNS:
    - Deserialization vulnerabilities
    - SQL Injection
    - Remote Code Execution (RCE)
    - Path Traversal
    - XML External Entity (XXE)
    - Server-Side Request Forgery (SSRF)
    - Authentication/Authorization issues
    - Cross-Site Scripting (XSS)
    - Log Injection (like Log4Shell)
    
    This is used by default when no --ai-provider is specified.
    """
    
    def __init__(self, exploit_db: 'ExploitDBIntelligence' = None):
        """Initialize with optional Exploit-DB intelligence source."""
        self.exploit_db = exploit_db
    
    EXPLOIT_PATTERNS = {
        'deserialization': {
            'keywords': ['deserializ', 'unmarshall', 'readObject', 'fromJson', 'parse'],
            'condition': 'Deserializes untrusted data',
            'severity_boost': True
        },
        'sql_injection': {
            'keywords': ['sql', 'query', 'execute', 'prepareStatement'],
            'condition': 'Executes SQL with user input',
            'severity_boost': True
        },
        'remote_code_execution': {
            'keywords': ['exec', 'eval', 'Runtime', 'ProcessBuilder', 'shell'],
            'condition': 'Executes commands or code',
            'severity_boost': True
        },
        'path_traversal': {
            'keywords': ['path', 'file', 'directory', '../', 'traversal'],
            'condition': 'Handles file paths from user input',
            'severity_boost': False
        },
        'xxe': {
            'keywords': ['xml', 'parse', 'DocumentBuilder', 'SAXParser', 'XMLReader'],
            'condition': 'Parses XML from untrusted source',
            'severity_boost': True
        },
        'ssrf': {
            'keywords': ['url', 'request', 'fetch', 'http', 'connect'],
            'condition': 'Makes HTTP requests with user-controlled URLs',
            'severity_boost': True
        },
        'ldap_injection': {
            'keywords': ['ldap', 'directory', 'jndi', 'lookup'],
            'condition': 'Performs LDAP lookups with user input',
            'severity_boost': True
        },
        'log_injection': {
            'keywords': ['log4j', 'log4shell', 'jndi', 'lookup'],
            'condition': 'Logs user-controlled data without sanitization',
            'severity_boost': True
        }
    }
    
    def analyze_exploit_conditions(self, cve_id: str, description: str,
                                   component: Component, code_context: str) -> ExploitAnalysis:
        """Analyze exploit conditions using heuristics + optional Exploit-DB intelligence."""
        
        description_lower = description.lower()
        code_lower = code_context.lower() if code_context else ""
        
        conditions = []
        conditions_met = []
        conditions_not_met = []
        reasoning_parts = []
        intel_score = 0
        
        # =====================================================================
        # PART 1: Exploit-DB Intelligence (OPTIONAL - enabled with --exploit-db)
        # =====================================================================
        intel_data_stored = {}
        if self.exploit_db and cve_id.startswith('CVE-'):
            exploitdb_data = self.exploit_db.get_exploit_data(cve_id)
            intel_data_stored = {'exploit_db': exploitdb_data}
            
            if exploitdb_data.get('has_exploit'):
                conditions.append("Public exploit code available on Exploit-DB")
                conditions_met.append(f"Exploit-DB: {exploitdb_data.get('exploit_count', 0)} public exploits found")
                reasoning_parts.append(f"🔥 Exploit-DB: {exploitdb_data.get('exploit_count', 0)} public exploits")
                intel_score += 40
                
                if exploitdb_data.get('verified'):
                    conditions_met.append("VERIFIED exploit on Exploit-DB")
                    reasoning_parts.append("✓ Exploit is VERIFIED by Exploit-DB")
                    intel_score += 20
                
                if exploitdb_data.get('metasploit'):
                    conditions_met.append("Metasploit module available")
                    reasoning_parts.append("⚡ Metasploit module exists")
                    intel_score += 15
                
                # Add trackable conditions from Exploit-DB
                for edb_cond in exploitdb_data.get('conditions', [])[:3]:
                    if edb_cond not in conditions:
                        conditions.append(edb_cond)
                        # Check if condition pattern exists in code
                        cond_lower = edb_cond.lower()
                        if code_context and any(kw in code_lower for kw in ['deserializ', 'lookup', 'exec', 'query', 'file', 'xml', 'url', 'http']):
                            if any(kw in cond_lower for kw in ['deserializ', 'jndi', 'command', 'sql', 'path', 'xml', 'ssrf', 'http']):
                                conditions_met.append(f"Code pattern: {edb_cond[:50]}")
        
        # =====================================================================
        # PART 2: Pattern-based Heuristic Analysis
        # =====================================================================
        for pattern_name, pattern_data in self.EXPLOIT_PATTERNS.items():
            pattern_in_desc = any(kw in description_lower for kw in pattern_data['keywords'])
            
            if pattern_in_desc:
                conditions.append(pattern_data['condition'])
                
                pattern_in_code = any(kw in code_lower for kw in pattern_data['keywords'])
                
                if pattern_in_code and code_context:
                    conditions_met.append(pattern_data['condition'])
                    reasoning_parts.append(f"{pattern_name} pattern found in code")
                elif not code_context:
                    reasoning_parts.append(f"{pattern_name} vulnerability - code analysis needed")
                else:
                    conditions_not_met.append(pattern_data['condition'])
                    reasoning_parts.append(f"{pattern_name} pattern not found in analyzed code")
        
        if not conditions:
            conditions.append("Vulnerable component version in use")
            if code_context:
                conditions_met.append("Component is imported and used")
            else:
                conditions_not_met.append("Component usage not detected")
        
        # =====================================================================
        # PART 3: Calculate Final Exploitability
        # =====================================================================
        # Intel score thresholds: 60+ = CRITICAL, 40+ = HIGH, 20+ = MEDIUM
        if intel_score >= 60:
            exploitability = "CRITICAL"
        elif intel_score >= 40:
            exploitability = "HIGH"
        elif intel_score >= 20 or len(conditions_met) > len(conditions_not_met):
            exploitability = "HIGH" if len(conditions_met) > len(conditions_not_met) else "MEDIUM"
        elif len(conditions_met) > 0:
            exploitability = "MEDIUM"
        elif code_context:
            exploitability = "LOW"
        else:
            exploitability = "LOW"
        
        return ExploitAnalysis(
            conditions=conditions,
            conditions_met=conditions_met,
            conditions_not_met=conditions_not_met,
            exploitability=exploitability,
            reasoning="; ".join(reasoning_parts) if reasoning_parts else "Heuristic analysis based on CVE description and code patterns",
            intel_data=intel_data_stored
        )


