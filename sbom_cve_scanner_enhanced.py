#
# FOUR OPTIONS ARE AVAILABLE:
# 1. HeuristicAnalyzer (DEFAULT) - Uses pattern matching and rules. NO API KEY NEEDED.
# 2. OpenAIProvider - Uses OpenAI GPT models. REQUIRES OPENAI_API_KEY.
# 3. GeminiProvider - Uses Google Gemini AI. REQUIRES GOOGLE_API_KEY.
# 4. OllamaProvider - Uses local Ollama LLM. REQUIRES LOCAL OLLAMA SERVER.
#
# By default, HeuristicAnalyzer is used which analyzes:
# - Vulnerability description keywords (deserialization, SQL injection, etc.)
# - Code patterns in the source files
# - Severity levels and attack vectors
# =============================================================================

class AIProvider(ABC):
    """
    Abstract base class for exploit condition analyzers.
    
    All analyzers (AI-based or heuristic) implement this interface,
    allowing them to be used interchangeably.
    """
    
    @abstractmethod
    def analyze_exploit_conditions(self, cve_id: str, description: str, 
                                   component: Component, code_context: str) -> ExploitAnalysis:
        """
        Analyze exploit conditions for a vulnerability.
        
        Args:
            cve_id: The CVE identifier (e.g., CVE-2021-44228)
            description: The vulnerability description text
            component: The affected component/dependency
            code_context: Relevant source code snippets (if available)
            
        Returns:
            ExploitAnalysis with conditions, exploitability level, and reasoning
        """
        pass


class OpenAIProvider(AIProvider):
    """
    OpenAI-based exploit condition analyzer.
    
    Uses GPT models to intelligently analyze CVE descriptions and code context
    to determine exploitability. Optionally enhanced with Exploit-DB intelligence.
    Requires an OpenAI API key.
    
    Usage:
        provider = OpenAIProvider(api_key="sk-...", exploit_db=exploit_db)
        analysis = provider.analyze_exploit_conditions(cve_id, desc, component, code)
    """
    
    def __init__(self, api_key: str, model: str = "gpt-4o-mini", exploit_db: 'ExploitDBIntelligence' = None):
        self.api_key = api_key
        self.model = model
        self.api_url = "https://api.openai.com/v1/chat/completions"
        self.exploit_db = exploit_db
    
    def analyze_exploit_conditions(self, cve_id: str, description: str,
                                   component: Component, code_context: str) -> ExploitAnalysis:
        """Use OpenAI to analyze exploit conditions."""
        
        prompt = self._build_prompt(cve_id, description, component, code_context)
        
        try:
            response = requests.post(
                self.api_url,
                headers={
                    "Authorization": f"Bearer {self.api_key}",
                    "Content-Type": "application/json"
                },
                json={
                    "model": self.model,
                    "messages": [
                        {
                            "role": "system",
                            "content": """You are a security expert analyzing CVE exploitability.
Analyze the CVE and code context to determine:
1. What conditions are required to exploit this vulnerability
2. Whether those conditions appear to be met in the code
3. The overall exploitability level

Respond in JSON format:
{
    "conditions": ["list of conditions required to exploit"],
    "conditions_met": ["conditions that appear to be met based on code"],
    "conditions_not_met": ["conditions that don't appear to be met"],
    "exploitability": "HIGH|MEDIUM|LOW",
    "reasoning": "brief explanation"
}"""
                        },
                        {"role": "user", "content": prompt}
                    ],
                    "temperature": 0.1,
                    "max_tokens": 1000
                },
                timeout=30
            )
            response.raise_for_status()
            
            result = response.json()
            content = result['choices'][0]['message']['content']
            
            json_match = re.search(r'\{[\s\S]*\}', content)
            if json_match:
                analysis_data = json.loads(json_match.group())
                return ExploitAnalysis(
                    conditions=analysis_data.get('conditions', []),
                    conditions_met=analysis_data.get('conditions_met', []),
                    conditions_not_met=analysis_data.get('conditions_not_met', []),
                    exploitability=analysis_data.get('exploitability', 'UNKNOWN'),
                    reasoning=analysis_data.get('reasoning', '')
                )
        
        except Exception as e:
            print(f"    AI analysis failed: {e}")
        
        return ExploitAnalysis(exploitability="UNKNOWN", reasoning="AI analysis unavailable")
    
    def _build_prompt(self, cve_id: str, description: str, 
                      component: Component, code_context: str) -> str:
        """Build the analysis prompt with optional Exploit-DB intelligence."""
        
        # Gather Exploit-DB intelligence if available
        intel_context = ""
        if self.exploit_db and cve_id.startswith('CVE-'):
            edb_data = self.exploit_db.get_exploit_data(cve_id)
            intel_parts = []
            
            if edb_data.get('has_exploit'):
                intel_parts.append(f"🔥 Exploit-DB: {edb_data.get('exploit_count', 0)} public exploits found")
                if edb_data.get('verified'):
                    intel_parts.append("✓ Exploit is VERIFIED by Exploit-DB")
                if edb_data.get('metasploit'):
                    intel_parts.append("⚡ Metasploit module available")
                
                # Add exploit conditions
                for cond in edb_data.get('conditions', [])[:3]:
                    intel_parts.append(f"  - {cond}")
            
            if intel_parts:
                intel_context = "\n\nExploit-DB Intelligence:\n" + "\n".join(intel_parts)
        
        return f"""Analyze the exploitability of this vulnerability:

CVE: {cve_id}
Component: {component.name} v{component.version}
Ecosystem: {component.ecosystem}

Vulnerability Description:
{description[:2000]}
{intel_context}

Code Context (imports and usage patterns found):
{code_context[:3000] if code_context else "No direct code usage found - dependency may be transitive"}

Determine:
1. What specific conditions must be met to exploit this CVE?
2. Based on the code context, are those conditions likely met?
3. What is the overall exploitability level?

Consider factors like:
- Is user input involved?
- Is the vulnerable function/feature actually used?
- Are there network exposure requirements?
- Are there authentication/authorization bypasses needed?
- Is deserialization of untrusted data involved?
- If public exploits exist on Exploit-DB (especially verified), prioritize HIGH exploitability"""


class OllamaProvider(AIProvider):
    """
    Ollama-based exploit condition analyzer using local LLM.
    
    Uses locally-running Ollama server with models like Llama, Mistral, etc.
    Optionally enhanced with Exploit-DB intelligence.
    No API key needed, but requires Ollama to be installed and running.
    
    Setup:
        1. Install Ollama: https://ollama.ai
        2. Pull a model: ollama pull llama3.2
        3. Start server: ollama serve (runs on http://localhost:11434)
    
    Usage:
        provider = OllamaProvider(model="llama3.2", exploit_db=exploit_db)
        analysis = provider.analyze_exploit_conditions(cve_id, desc, component, code)
    """
    
    def __init__(self, model: str = "llama3.2", base_url: str = "http://localhost:11434", exploit_db: 'ExploitDBIntelligence' = None):
        self.model = model
        self.base_url = base_url
        self.exploit_db = exploit_db
    
    def analyze_exploit_conditions(self, cve_id: str, description: str,
                                   component: Component, code_context: str) -> ExploitAnalysis:
        """Use Ollama to analyze exploit conditions."""
        
