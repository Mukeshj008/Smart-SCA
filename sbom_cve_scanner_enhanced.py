# VULNERABILITY SCANNER
# =============================================================================
# Fetches vulnerability data from the OSV (Open Source Vulnerabilities)
# database. OSV is a free, open database of security vulnerabilities.
# =============================================================================

class VulnerabilityScanner:
    """
    Fetches vulnerability data from the OSV (Open Source Vulnerabilities) API.
    
    OSV (https://osv.dev) is a distributed vulnerability database for open source
    software. It aggregates data from multiple sources including:
    - GitHub Security Advisories (GHSA)
    - National Vulnerability Database (NVD/CVE)
    - Python Security Advisories (PSA)
    - RustSec Advisory Database
    - Go Vulnerability Database
    
    HOW IT WORKS:
    1. For each component, queries the OSV API with package name and version
    2. OSV returns matching vulnerabilities with severity, description, and fixes
    3. Results include aliases (CVE IDs, GHSA IDs) for cross-referencing
    
    RATE LIMITING:
    - Small delay between requests to avoid overwhelming the API
    - Uses connection pooling for efficiency
    
    SSL NOTE:
    - Some systems have SSL certificate issues with the API
    - Use --no-verify-ssl flag as a workaround (not recommended for production)
    
    Example:
        scanner = VulnerabilityScanner()
        vulns = scanner.scan_component(component)  # Returns list of Vulnerability
    """
    
    OSV_API_URL = "https://api.osv.dev/v1/query"
    
    ECOSYSTEM_MAP = {
        'npm': 'npm',
        'PyPI': 'PyPI',
        'Maven': 'Maven',
        'NuGet': 'NuGet',
        'RubyGems': 'RubyGems',
        'Go': 'Go',
        'crates.io': 'crates.io',
        'Packagist': 'Packagist',
    }
    
    def __init__(self, rate_limit_delay: float = 0.3, verify_ssl: bool = True):
        self.rate_limit_delay = rate_limit_delay
        self.session = requests.Session()
        self.session.headers.update({
            'Content-Type': 'application/json',
            'User-Agent': 'SBOM-CVE-Scanner-Enhanced/2.0'
        })
        
        if verify_ssl:
            try:
                import certifi
                self.session.verify = certifi.where()
            except ImportError:
                self.session.verify = True
        else:
            self.session.verify = False
            import urllib3
            urllib3.disable_warnings(urllib3.exceptions.InsecureRequestWarning)
    
    def scan_component(self, component: Component) -> list[Vulnerability]:
        """Scan a single component for vulnerabilities."""
        vulnerabilities = []
        
        if component.purl:
            vulns = self._query_by_purl(component.purl)
            vulnerabilities.extend(vulns)
        elif component.ecosystem != "unknown":
            vulns = self._query_by_package(
                component.name,
                component.version,
                component.ecosystem
            )
            vulnerabilities.extend(vulns)
        
        time.sleep(self.rate_limit_delay)
        return vulnerabilities
    
    def _query_by_purl(self, purl: str) -> list[Vulnerability]:
        """Query OSV by Package URL."""
        payload = {"package": {"purl": purl}}
        return self._execute_query(payload)
    
    def _query_by_package(self, name: str, version: str, ecosystem: str) -> list[Vulnerability]:
        """Query OSV by package name and version."""
        osv_ecosystem = self.ECOSYSTEM_MAP.get(ecosystem)
        if not osv_ecosystem:
            return []
        
        payload = {
            "package": {
                "name": name,
                "ecosystem": osv_ecosystem
            }
        }
        
        if version:
            payload["version"] = version
        
        return self._execute_query(payload)
    
    def _execute_query(self, payload: dict) -> list[Vulnerability]:
        """Execute OSV API query and parse results."""
        try:
            response = self.session.post(self.OSV_API_URL, json=payload, timeout=30)
            response.raise_for_status()
            data = response.json()
            
            vulnerabilities = []
            for vuln in data.get('vulns', []):
                vulnerability = self._parse_vulnerability(vuln)
                if vulnerability:
                    vulnerabilities.append(vulnerability)
            
            return vulnerabilities
            
        except requests.RequestException:
            return []
        except json.JSONDecodeError:
            return []
    
    def _parse_vulnerability(self, vuln_data: dict) -> Optional[Vulnerability]:
        """Parse OSV vulnerability data."""
        vuln_id = vuln_data.get('id', '')
        
        severity = "UNKNOWN"
        cvss_score = None
        
        if 'severity' in vuln_data:
            for sev in vuln_data['severity']:
                if sev.get('type') == 'CVSS_V3':
                    score_str = sev.get('score', '')
                    try:
                        if '/' in score_str:
                            cvss_score = float(score_str.split('/')[0])
                    except (ValueError, IndexError):
                        pass
        
        if 'database_specific' in vuln_data:
            db_specific = vuln_data['database_specific']
            if 'severity' in db_specific:
                severity = db_specific['severity'].upper()
            if 'cvss' in db_specific and cvss_score is None:
                cvss_score = db_specific['cvss'].get('score')
        
        if cvss_score and severity == "UNKNOWN":
            severity = self._cvss_to_severity(cvss_score)
        
        description = vuln_data.get('summary', '') or vuln_data.get('details', '')[:500]
        
        references = []
        for ref in vuln_data.get('references', []):
            references.append({
                'type': ref.get('type', 'WEB'),
                'url': ref.get('url', '')
            })
        
        affected_versions = []
        fixed_versions = []
        for affected in vuln_data.get('affected', []):
            for rng in affected.get('ranges', []):
                for event in rng.get('events', []):
                    if 'introduced' in event:
                        affected_versions.append(f">= {event['introduced']}")
                    if 'fixed' in event:
                        fixed_versions.append(event['fixed'])
        
        aliases = vuln_data.get('aliases', [])
        
        return Vulnerability(
            cve_id=vuln_id,
            severity=severity,
            cvss_score=cvss_score,
            description=description,
            references=references,
            affected_versions=", ".join(affected_versions),
            fixed_versions=", ".join(fixed_versions),
            aliases=aliases
        )
    
    def _cvss_to_severity(self, score: float) -> str:
        """Convert CVSS score to severity."""
        if score >= 9.0:
            return "CRITICAL"
        elif score >= 7.0:
            return "HIGH"
        elif score >= 4.0:
            return "MEDIUM"
        elif score > 0:
            return "LOW"
        return "UNKNOWN"


