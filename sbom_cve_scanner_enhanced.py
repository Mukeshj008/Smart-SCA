    def _get_mitigation(self, vuln: Vulnerability, comp: Component) -> str:
        parts = []
        if vuln.fixed_versions:
            parts.append(f"<strong>Upgrade to:</strong> {html.escape(vuln.fixed_versions)}")
        else:
            parts.append("<strong>Check for updates</strong>")
        
        for ref in vuln.references:
            if ref.get('type') == 'ADVISORY':
                parts.append(f'<a href="{html.escape(ref["url"])}" target="_blank">Advisory</a>')
                break
        
        return "<br>".join(parts)
    
