# REACHABILITY ANALYZER
# =============================================================================
# Performs static code analysis to determine if vulnerable dependencies are
# actually imported/used in the codebase. If a dependency isn't reachable,
# the vulnerability has lower exploitability.
# =============================================================================

class ReachabilityAnalyzer:
    """
    Performs static code analysis to determine if dependencies are reachable.
    
    This analyzer scans source code files for import statements to detect
    if a dependency is actually being used. A vulnerability in an unused
    dependency has lower exploitability.
    
    SUPPORTED LANGUAGES:
    - Java: import statements, package patterns
    - Python: import and from...import statements
    - JavaScript/TypeScript: require() and import statements
    - Go: import statements
    
    HOW IT WORKS:
    1. Scans all source files in the provided directories
    2. Extracts all import statements using regex patterns
    3. Builds an index of imported packages
    4. Checks each dependency against the import index
    
    CONFIDENCE LEVELS:
    - HIGH: Multiple import locations found with clear patterns
    - MEDIUM: Some imports found but patterns unclear
    - LOW: No direct imports detected (may be transitive dependency)
    
    Example:
        analyzer = ReachabilityAnalyzer(["/path/to/src"])
        result = analyzer.analyze(component)  # Returns ReachabilityAnalysis
    """
    
    JAVA_IMPORT_PATTERN = re.compile(r'import\s+(?:static\s+)?([a-zA-Z0-9_.]+(?:\.\*)?)\s*;')
    PYTHON_IMPORT_PATTERN = re.compile(r'(?:from\s+([a-zA-Z0-9_.]+)\s+import|import\s+([a-zA-Z0-9_.]+))')
    JS_IMPORT_PATTERN = re.compile(r'(?:import\s+.*?from\s+[\'"]([^"\']+)[\'"]|require\s*\(\s*[\'"]([^"\']+)[\'"]\s*\))')
    GO_IMPORT_PATTERN = re.compile(r'import\s+(?:\(\s*)?["\']([^"\']+)["\']')
    
    PACKAGE_MAPPINGS = {
        'commons-beanutils': ['org.apache.commons.beanutils'],
        'commons-lang3': ['org.apache.commons.lang3'],
        'jackson-databind': ['com.fasterxml.jackson.databind', 'com.fasterxml.jackson'],
        'jackson-core': ['com.fasterxml.jackson.core'],
        'log4j-core': ['org.apache.logging.log4j'],
        'log4j-api': ['org.apache.logging.log4j'],
        'elasticsearch': ['org.elasticsearch'],
        'spring-core': ['org.springframework'],
        'spring-beans': ['org.springframework.beans'],
        'spring-context': ['org.springframework.context'],
        'spring-webmvc': ['org.springframework.web'],
        'spring-expression': ['org.springframework.expression'],
        'hibernate-validator': ['org.hibernate.validator', 'javax.validation'],
        'gson': ['com.google.gson'],
        'quartz': ['org.quartz'],
        'liquibase-core': ['liquibase'],
        'spring-data-commons': ['org.springframework.data'],
        'django': ['django'],
        'requests': ['requests'],
        'flask': ['flask'],
        'express': ['express'],
        'lodash': ['lodash'],
        'axios': ['axios'],
    }
    
    def __init__(self, source_paths: list[str]):
        self.source_paths = source_paths
        self.file_cache: dict[str, str] = {}
        self.import_index: dict[str, list[tuple[str, int]]] = {}
        self._build_import_index()
    
    def _build_import_index(self):
        """Build an index of all imports in the codebase."""
        for source_path in self.source_paths:
            path = Path(source_path)
            if not path.exists():
                continue
                
            for file_path in self._get_source_files(path):
                try:
                    content = file_path.read_text(errors='ignore')
                    self.file_cache[str(file_path)] = content
                    self._index_file_imports(str(file_path), content)
                except Exception:
                    continue
    
    def _get_source_files(self, path: Path):
        """Get all source files from a directory."""
        extensions = {'.java', '.py', '.js', '.ts', '.tsx', '.jsx', '.go', '.kt', '.scala'}
        
        if path.is_file():
            if path.suffix in extensions:
                yield path
        else:
            for ext in extensions:
                yield from path.rglob(f'*{ext}')
    
    def _index_file_imports(self, file_path: str, content: str):
        """Index imports from a single file."""
        imports = []
        
        if file_path.endswith('.java') or file_path.endswith('.kt') or file_path.endswith('.scala'):
            for match in self.JAVA_IMPORT_PATTERN.finditer(content):
                imports.append(match.group(1))
        
        elif file_path.endswith('.py'):
            for match in self.PYTHON_IMPORT_PATTERN.finditer(content):
                imp = match.group(1) or match.group(2)
                if imp:
                    imports.append(imp)
        
        elif file_path.endswith(('.js', '.ts', '.tsx', '.jsx')):
            for match in self.JS_IMPORT_PATTERN.finditer(content):
                imp = match.group(1) or match.group(2)
                if imp:
                    imports.append(imp)
        
        elif file_path.endswith('.go'):
            for match in self.GO_IMPORT_PATTERN.finditer(content):
                imports.append(match.group(1))
        
        for imp in imports:
            if imp not in self.import_index:
                self.import_index[imp] = []
            line_num = content[:content.find(imp)].count('\n') + 1
            self.import_index[imp].append((file_path, line_num))
    
    def analyze(self, component: Component) -> ReachabilityAnalysis:
        """Analyze if a component is reachable in the codebase."""
        result = ReachabilityAnalysis()
        
        search_patterns = self._get_search_patterns(component)
        
        for pattern in search_patterns:
            for indexed_import, locations in self.import_index.items():
                if self._matches_pattern(indexed_import, pattern):
                    result.is_reachable = True
                    for file_path, line_num in locations:
                        result.import_locations.append(f"{file_path}:{line_num}")
                        result.usage_patterns.append(indexed_import)
        
        result.import_locations = list(set(result.import_locations))[:10]
        result.usage_patterns = list(set(result.usage_patterns))[:5]
        
        if result.is_reachable:
            if len(result.import_locations) >= 3:
                result.confidence = "HIGH"
            elif len(result.import_locations) >= 1:
                result.confidence = "MEDIUM"
            else:
                result.confidence = "LOW"
        else:
            result.confidence = "MEDIUM"
        
        return result
    
    def _get_search_patterns(self, component: Component) -> list[str]:
        """Get import patterns to search for a component."""
        patterns = []
        
        name_lower = component.name.lower()
        if name_lower in self.PACKAGE_MAPPINGS:
            patterns.extend(self.PACKAGE_MAPPINGS[name_lower])
        
        if component.group_id:
            patterns.append(component.group_id)
            patterns.append(f"{component.group_id}.{component.name}")
        
        patterns.append(component.name)
        patterns.append(component.name.replace('-', '_'))
        patterns.append(component.name.replace('-', '.'))
        
        return patterns
    
    def _matches_pattern(self, import_stmt: str, pattern: str) -> bool:
        """Check if an import statement matches a pattern."""
        import_lower = import_stmt.lower()
        pattern_lower = pattern.lower()
        
        return (
            import_lower == pattern_lower or
            import_lower.startswith(pattern_lower + '.') or
            import_lower.startswith(pattern_lower + '/') or
            pattern_lower in import_lower
        )


