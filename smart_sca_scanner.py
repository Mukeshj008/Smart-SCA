class ExploitDBIntelligence:
    """
    Fetches exploit intelligence from Exploit-DB.
    
    NO API KEY REQUIRED!
    
    Exploit-DB (exploit-db.com) is a CVE-compliant archive of public exploits
    maintained by OffSec. It contains:
    - Public exploits and PoCs
    - Metasploit modules
    - Shellcodes
    - Papers
    
    Usage:
        intel = ExploitDBIntelligence()
        data = intel.get_exploit_data("CVE-2021-44228")
        print(data['has_exploit'])  # True
        print(data['exploits'])  # List of exploit references
    """
    
    EXPLOITDB_SEARCH_URL = "https://www.exploit-db.com/search"
    
    def __init__(self):
        self._cache = {}
    
    def get_exploit_data(self, cve_id: str) -> dict:
        """
        Search Exploit-DB for exploits related to a CVE.
        
        Returns exploit conditions that can be tracked in code.
        """
        if not cve_id.startswith('CVE-'):
            return {'has_exploit': False, 'exploits': [], 'conditions': []}
        
        if cve_id in self._cache:
            return self._cache[cve_id]
        
        result = {
            'has_exploit': False,
            'exploit_count': 0,
            'exploits': [],
            'conditions': [],
            'metasploit': False,
            'verified': False,
            'search_url': f"https://www.exploit-db.com/search?cve={cve_id.replace('CVE-', '')}"
        }
        
        try:
            # Extract CVE number (e.g., "2019-16335" from "CVE-2019-16335")
            cve_num = cve_id.replace('CVE-', '')
            
            # Query Exploit-DB's JSON API
            response = requests.get(
                self.EXPLOITDB_SEARCH_URL,
                params={
                    'cve': cve_num,
                    'draw': 1,
                    'start': 0,
                    'length': 10
                },
                headers={
                    'User-Agent': 'Mozilla/5.0 (compatible; SBOM-Scanner/2.0)',
                    'Accept': 'application/json',
                    'X-Requested-With': 'XMLHttpRequest'
                },
                timeout=15
            )
            response.raise_for_status()
            
            data = response.json()
            
            if data.get('data') and len(data['data']) > 0:
                result['has_exploit'] = True
                result['exploit_count'] = len(data['data'])
                
                for exploit in data['data'][:5]:  # Limit to 5 exploits
                    exploit_info = {
                        'id': exploit.get('id', ''),
                        'title': self._clean_html(exploit.get('description', {}).get('title', '')),
                        'type': exploit.get('type', {}).get('type_name', 'Unknown'),
                        'platform': exploit.get('platform', {}).get('platform', 'Multiple'),
                        'date': exploit.get('date_published', ''),
                        'verified': exploit.get('verified', False),
                        'url': f"https://www.exploit-db.com/exploits/{exploit.get('id', '')}"
                    }
                    result['exploits'].append(exploit_info)
                    
                    # Check for Metasploit
                    if 'metasploit' in exploit_info['title'].lower() or 'msf' in exploit_info['title'].lower():
                        result['metasploit'] = True
                    
                    # Check if verified
                    if exploit.get('verified'):
                        result['verified'] = True
                    
                    # Extract conditions from title/type
                    conditions = self._extract_conditions(exploit_info['title'], exploit_info['type'])
                    for cond in conditions:
                        if cond not in result['conditions']:
                            result['conditions'].append(cond)
            
            self._cache[cve_id] = result
            return result
            
        except Exception as e:
            # If API fails, still return the search URL
            self._cache[cve_id] = result
            return result
    
    def _clean_html(self, text: str) -> str:
        """Remove HTML tags from text."""
        import re
        clean = re.sub(r'<[^>]+>', '', str(text))
        return clean.strip()
    
    def _extract_conditions(self, title: str, exploit_type: str) -> list:
        """Extract trackable exploit conditions from exploit metadata."""
        conditions = []
        text = (title + ' ' + exploit_type).lower()
        
        # Remote Code Execution
        if any(x in text for x in ['remote code', 'rce', 'code execution']):
            conditions.append("Remote Code Execution (check for eval, exec, ProcessBuilder, Runtime.exec)")
        
        # Deserialization
        if any(x in text for x in ['deserializ', 'unmarshal', 'object injection']):
            conditions.append("Deserialization vulnerability (check for ObjectInputStream, readObject, fromJson)")
        
        # SQL Injection
        if any(x in text for x in ['sql injection', 'sqli', 'blind sql']):
            conditions.append("SQL Injection (check for dynamic SQL, string concatenation in queries)")
        
        # Command Injection
        if any(x in text for x in ['command injection', 'os command', 'shell']):
            conditions.append("Command Injection (check for system(), exec(), shell commands)")
        
        # Path Traversal
        if any(x in text for x in ['path traversal', 'directory traversal', 'lfi', 'local file']):
            conditions.append("Path Traversal (check for file operations with user input)")
        
        # XXE
        if any(x in text for x in ['xxe', 'xml external', 'xml injection']):
            conditions.append("XXE (check for XML parsing without disabling external entities)")
        
        # SSRF
        if any(x in text for x in ['ssrf', 'server-side request']):
            conditions.append("SSRF (check for HTTP requests with user-controlled URLs)")
        
        # Authentication Bypass
        if any(x in text for x in ['auth bypass', 'authentication bypass', 'privilege']):
            conditions.append("Authentication Bypass (check authentication/authorization logic)")
        
        # Buffer Overflow
        if any(x in text for x in ['buffer overflow', 'stack overflow', 'heap overflow']):
            conditions.append("Buffer Overflow (check for unsafe memory operations)")
        
        # DoS
        if any(x in text for x in ['denial of service', 'dos', 'crash']):
            conditions.append("Denial of Service (check for resource exhaustion, infinite loops)")
        
        # XSS
        if any(x in text for x in ['xss', 'cross-site scripting', 'script injection']):
            conditions.append("XSS (check for unsanitized output in HTML)")
        
        # JNDI/LDAP
        if any(x in text for x in ['jndi', 'ldap', 'log4j', 'log4shell']):
            conditions.append("JNDI Injection (check for JNDI lookups with user input)")
        
        return conditions
    
    def _calculate_exploitability(self, intel_data: dict) -> dict:
        """Calculate exploitability based on Exploit-DB data."""
        score = 0
        reasons = []
        conditions = intel_data.get('conditions', [])
        
        if intel_data.get('has_exploit'):
            score += 40
            reasons.append(f"Public exploit available ({intel_data.get('exploit_count', 0)} found on Exploit-DB)")
            
            if intel_data.get('verified'):
                score += 20
                reasons.append("Exploit is VERIFIED by Exploit-DB")
            
            if intel_data.get('metasploit'):
                score += 15
                reasons.append("Metasploit module available")
        
        # Determine level
        if score >= 60:
            level = "CRITICAL"
        elif score >= 40:
            level = "HIGH"
        elif score >= 20:
            level = "MEDIUM"
        else:
            level = "LOW"
        
        return {
            'level': level,
            'score': score,
            'reasons': reasons,
            'conditions': conditions
        }




