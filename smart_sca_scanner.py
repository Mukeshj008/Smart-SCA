class GeminiProvider(AIProvider):
    """
    Google Gemini-based exploit condition analyzer.
    
    Uses Google's Gemini AI models to analyze CVE descriptions and code context
    to determine exploitability. Optionally enhanced with Exploit-DB intelligence.
    Requires a Google AI API key.
    
    Setup:
        1. Get an API key from: https://aistudio.google.com/app/apikey
        2. Or from Google Cloud Console with Vertex AI enabled
    
    Usage:
        provider = GeminiProvider(api_key="your-api-key", exploit_db=exploit_db)
        analysis = provider.analyze_exploit_conditions(cve_id, desc, component, code)
    
    Supported models (as of 2025):
        - gemini-2.0-flash (default, fast and cost-effective)
        - gemini-2.5-flash (latest, more capable)
        - gemini-2.5-pro (most capable, higher cost)
    """
    
    def __init__(self, api_key: str, model: str = "gemini-2.0-flash", exploit_db: 'ExploitDBIntelligence' = None, use_grounding: bool = False):
        self.api_key = api_key
        self.model = model
        self.api_url = f"https://generativelanguage.googleapis.com/v1beta/models/{model}:generateContent"
        self.request_count = 0
        self.last_request_time = 0
        self.exploit_db = exploit_db
        self.use_grounding = use_grounding
    
    def analyze_exploit_conditions(self, cve_id: str, description: str,
                                   component: Component, code_context: str) -> ExploitAnalysis:
        """Use Gemini to extract exploit conditions from CVE description. If insufficient, retry with Google Search."""
        # Step 1: Try extraction from description only (no grounding), unless user requested grounding from start
        use_grounding_first = getattr(self, 'use_grounding', False)
        result = self._call_gemini_extract(cve_id, description, component, code_context, use_grounding=use_grounding_first)
        # Step 2: If insufficient, retry with Google Search to find conditions from internet (NVD, advisories, PoC)
        if self._is_insufficient(result) and not use_grounding_first:
            print(f"    CVE description insufficient for {cve_id}, searching internet for exploit conditions...")
            current_time = time.time()
            if current_time - self.last_request_time < 6.0:
                time.sleep(6.0 - (current_time - self.last_request_time))
            self.last_request_time = time.time()
            result = self._call_gemini_extract(cve_id, description, component, code_context, use_grounding=True)
        return result

    def _is_insufficient(self, analysis: ExploitAnalysis) -> bool:
        """True if conditions are empty or too generic to verify in code."""
        if not analysis.conditions:
            return True
        generic = {'vulnerable component in use', 'component is imported', 'component version in use'}
        specific_count = sum(1 for c in analysis.conditions
                            if not any(g in c.lower() for g in generic) and len(c) > 20)
        return specific_count < 2

    def _call_gemini_extract(self, cve_id: str, description: str, component: Component,
                             code_context: str, use_grounding: bool) -> ExploitAnalysis:
        """Single Gemini API call for condition extraction. use_grounding enables Google Search."""
        current_time = time.time()
        time_since_last = current_time - self.last_request_time
        delay = 6.0 if use_grounding else 4.0
        if time_since_last < delay:
            time.sleep(delay - time_since_last)
        self.last_request_time = time.time()
        self.request_count += 1

        prompt = self._build_extraction_prompt(cve_id, description, component, use_grounding=use_grounding)
        payload = {
            "contents": [{"parts": [{"text": prompt}]}],
            "generationConfig": {
                "temperature": 0.1,
                "maxOutputTokens": 1024,
                "topP": 0.8,
                "topK": 40
            },
            "safetySettings": [
                {"category": "HARM_CATEGORY_HARASSMENT", "threshold": "BLOCK_NONE"},
                {"category": "HARM_CATEGORY_HATE_SPEECH", "threshold": "BLOCK_NONE"},
                {"category": "HARM_CATEGORY_SEXUALLY_EXPLICIT", "threshold": "BLOCK_NONE"},
                {"category": "HARM_CATEGORY_DANGEROUS_CONTENT", "threshold": "BLOCK_NONE"}
            ]
        }
        if use_grounding:
            payload["tools"] = [{"googleSearch": {}}]

        max_retries = 3
        for retry in range(max_retries):
            try:
                response = requests.post(
                    f"{self.api_url}?key={self.api_key}",
                    headers={"Content-Type": "application/json"},
                    json=payload,
                    timeout=60
                )
                response.raise_for_status()
                result = response.json()
                if 'candidates' in result and len(result['candidates']) > 0:
                    content = result['candidates'][0].get('content', {})
                    parts = content.get('parts', [])
                    if parts:
                        text = parts[0].get('text', '')
                        json_match = re.search(r'\{[\s\S]*\}', text)
                        if json_match:
                            analysis_data = json.loads(json_match.group())
                            conditions = analysis_data.get('conditions', [])
                            exploitability = analysis_data.get('exploitability', 'UNKNOWN')
                            reasoning = analysis_data.get('reasoning', '')
                            # Use Gemini to verify conditions in code when source is available
                            if conditions and code_context:
                                conditions_met, conditions_not_met, conditions_met_locations = self.verify_conditions_in_code(conditions, code_context)
                            else:
                                conditions_met, conditions_not_met, conditions_met_locations = _check_conditions_in_code(conditions, code_context)
                            return ExploitAnalysis(
                                conditions=conditions,
                                conditions_met=conditions_met,
                                conditions_not_met=conditions_not_met,
                                conditions_met_locations=conditions_met_locations,
                                exploitability=exploitability,
                                reasoning=reasoning
                            )
                break

            except requests.exceptions.HTTPError as e:
                if e.response.status_code == 429:
                    # Rate limited - wait and retry
                    wait_time = (retry + 1) * 10  # 10, 20, 30 seconds
                    if retry < max_retries - 1:
                        print(f"    Rate limited, waiting {wait_time}s before retry...")
                        time.sleep(wait_time)
                        continue
                    else:
                        print(f"    Gemini API error: Rate limit exceeded after {max_retries} retries")
                elif e.response.status_code == 400:
                    print(f"    Gemini API error: Invalid request - check API key and model name")
                elif e.response.status_code == 403:
                    print(f"    Gemini API error: Access forbidden - check API key permissions")
                else:
                    print(f"    Gemini API error: {e}")
                break
            except Exception as e:
                print(f"    Gemini analysis failed: {e}")
                break
        
        return ExploitAnalysis(exploitability="UNKNOWN", reasoning="Gemini analysis unavailable")
    
