class EnhancedHTMLReportGenerator:
    """
    Generates an interactive HTML vulnerability report.
    
    FEATURES:
    - Summary cards showing vulnerability counts by severity and risk
    - Sortable table with all vulnerabilities
    - Clickable REACHABLE/EXCLUDED badges showing evidence in modal popups
    - CVE IDs linked to NVD and GitHub Advisory databases
    - Color-coded severity and risk levels
    - Excluded dependencies highlighted with orange background
    
    INTERACTIVITY:
    - Click on REACHABLE badge to see file paths where dependency is imported
    - Click on EXCLUDED badge to see pom.xml files with exclusion rules
    - Click outside modal or press Escape to close
    - All text in modals is selectable/copyable
    
    OUTPUT:
    - Single self-contained HTML file (no external dependencies)
    - Responsive design for different screen sizes
    """
    
    def generate(self, components: list[Component], output_path: str, sbom_file: str,
                 source_paths: list[str], ai_provider: str):
        """Generate enhanced HTML report."""
        
        total_vulns = sum(len(c.vulnerabilities) for c in components)
        vuln_components = [c for c in components if c.vulnerabilities]
        
        severity_counts = {"CRITICAL": 0, "HIGH": 0, "MEDIUM": 0, "LOW": 0, "UNKNOWN": 0}
        risk_counts = {"CRITICAL": 0, "HIGH": 0, "MEDIUM": 0, "LOW": 0, "NA": 0, "INFORMATIONAL": 0}
        reachable_count = 0
        excluded_count = 0
        
        for comp in components:
            for vuln in comp.vulnerabilities:
                sev = vuln.severity.upper()
                if sev in severity_counts:
                    severity_counts[sev] += 1
                
                risk = vuln.effective_risk
                if risk in risk_counts:
                    risk_counts[risk] += 1
                
                if vuln.reachability.is_reachable:
                    reachable_count += 1
                
                if vuln.exclusion_info.is_excluded:
                    excluded_count += 1
        
        html_content = self._build_html(
            components=components,
            vuln_components=vuln_components,
            total_vulns=total_vulns,
            severity_counts=severity_counts,
            risk_counts=risk_counts,
            reachable_count=reachable_count,
            excluded_count=excluded_count,
            sbom_file=sbom_file,
            source_paths=source_paths,
            ai_provider=ai_provider
        )
        
        Path(output_path).write_text(html_content)
        print(f"\nReport generated: {output_path}")
    
    def _build_pie_chart_svg(self, data: dict, colors: dict, size: int = 160, title: str = "") -> str:
        """Generate an inline SVG donut/pie chart."""
        import math
        total = sum(data.values())
        if total == 0:
            return f'<div style="text-align:center;padding:20px;color:#999;">No data</div>'
        
        cx, cy, r = size // 2, size // 2, size // 2 - 10
        inner_r = r * 0.55
        paths = []
        legend_items = []
        start_angle = -90
        
        for label, value in data.items():
            if value == 0:
                continue
            pct = value / total
            end_angle = start_angle + pct * 360
            large_arc = 1 if pct > 0.5 else 0
            
            x1 = cx + r * math.cos(math.radians(start_angle))
            y1 = cy + r * math.sin(math.radians(start_angle))
            x2 = cx + r * math.cos(math.radians(end_angle))
            y2 = cy + r * math.sin(math.radians(end_angle))
            
            ix1 = cx + inner_r * math.cos(math.radians(start_angle))
            iy1 = cy + inner_r * math.sin(math.radians(start_angle))
            ix2 = cx + inner_r * math.cos(math.radians(end_angle))
            iy2 = cy + inner_r * math.sin(math.radians(end_angle))
            
            color = colors.get(label, '#999')
            
            if pct >= 0.999:
                paths.append(f'<circle cx="{cx}" cy="{cy}" r="{r}" fill="{color}" />')
                paths.append(f'<circle cx="{cx}" cy="{cy}" r="{inner_r}" fill="white" />')
            else:
                path = (f'<path d="M {x1:.1f} {y1:.1f} '
                        f'A {r} {r} 0 {large_arc} 1 {x2:.1f} {y2:.1f} '
                        f'L {ix2:.1f} {iy2:.1f} '
                        f'A {inner_r} {inner_r} 0 {large_arc} 0 {ix1:.1f} {iy1:.1f} Z" '
                        f'fill="{color}" stroke="white" stroke-width="1.5"/>')
                paths.append(path)
            
            legend_items.append(
                f'<div style="display:flex;align-items:center;gap:6px;font-size:0.78rem;">'
                f'<span style="width:10px;height:10px;border-radius:50%;background:{color};display:inline-block;flex-shrink:0;"></span>'
                f'<span style="color:#555;">{label}</span>'
                f'<span style="font-weight:600;margin-left:auto;">{value} ({pct*100:.0f}%)</span>'
                f'</div>'
            )
            
