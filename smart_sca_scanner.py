# =============================================================================
# Parses Maven pom.xml files to detect if dependencies are explicitly excluded
# by developers. This helps reduce false positives by identifying dependencies
# that won't be included in the final build.
# =============================================================================

class ExclusionAnalyzer:
    """
    Analyzes Maven pom.xml files to detect dependency exclusions.
    
    This class parses all pom.xml files in the source directories and builds
    an index of excluded dependencies. It detects:
    - Direct exclusions using <exclusions> tags
    - Scope-based exclusions (provided, test, system scopes)
    - Dependency management exclusions
    
    Example pom.xml exclusion:
        <dependency>
            <groupId>com.example</groupId>
            <artifactId>my-lib</artifactId>
            <exclusions>
                <exclusion>
                    <groupId>org.vulnerable</groupId>
                    <artifactId>bad-lib</artifactId>
                </exclusion>
            </exclusions>
        </dependency>
    """
    
    MAVEN_NS = {'m': 'http://maven.apache.org/POM/4.0.0'}
    
    def __init__(self, source_paths: list[str]):
        self.source_paths = source_paths
        self.exclusions: dict[str, list[dict]] = {}
        self.dependency_management: dict[str, dict] = {}
        self._parse_all_poms()
    
    def _parse_all_poms(self):
        """Parse all pom.xml files to build exclusion index."""
        for source_path in self.source_paths:
            path = Path(source_path)
            if not path.exists():
                continue
            
            pom_files = list(path.rglob('pom.xml'))
            for pom_file in pom_files:
                try:
                    self._parse_pom(pom_file)
                except Exception as e:
                    print(f"  Warning: Could not parse {pom_file}: {e}")
    
    def _parse_pom(self, pom_path: Path):
        """Parse a single pom.xml file for exclusions."""
        try:
            tree = ET.parse(pom_path)
            root = tree.getroot()
            
            ns = self.MAVEN_NS if root.tag.startswith('{') else {}
            ns_prefix = 'm:' if ns else ''
            
            for dep in root.findall(f'.//{ns_prefix}dependency', ns) if ns else root.iter('dependency'):
                group_id = self._get_text(dep, 'groupId', ns)
                artifact_id = self._get_text(dep, 'artifactId', ns)
                
                if not artifact_id:
                    continue
                
                exclusions_elem = dep.find(f'{ns_prefix}exclusions' if ns else 'exclusions', ns) if ns else dep.find('exclusions')
                if exclusions_elem is None:
                    exclusions_elem = dep.find('exclusions')
                
                if exclusions_elem is not None:
                    for excl in exclusions_elem.findall(f'{ns_prefix}exclusion' if ns else 'exclusion', ns) if ns else exclusions_elem.findall('exclusion'):
                        excl_group = self._get_text(excl, 'groupId', ns)
                        excl_artifact = self._get_text(excl, 'artifactId', ns)
                        
                        if excl_artifact:
                            key = self._make_key(excl_group, excl_artifact)
                            if key not in self.exclusions:
                                self.exclusions[key] = []
                            self.exclusions[key].append({
                                'excluded_from': f"{group_id}:{artifact_id}",
                                'pom_file': str(pom_path),
                                'group_id': excl_group,
                                'artifact_id': excl_artifact
                            })
            
            dep_mgmt = root.find(f'.//{ns_prefix}dependencyManagement' if ns else './/dependencyManagement', ns) if ns else root.find('.//dependencyManagement')
            if dep_mgmt is not None:
                for dep in dep_mgmt.findall(f'.//{ns_prefix}dependency' if ns else './/dependency', ns) if ns else dep_mgmt.findall('.//dependency'):
                    group_id = self._get_text(dep, 'groupId', ns)
                    artifact_id = self._get_text(dep, 'artifactId', ns)
                    scope = self._get_text(dep, 'scope', ns)
                    
                    if artifact_id and scope in ('provided', 'test', 'system'):
                        key = self._make_key(group_id, artifact_id)
                        self.dependency_management[key] = {
                            'scope': scope,
                            'pom_file': str(pom_path)
                        }
        except ET.ParseError:
            pass
    
    def _get_text(self, elem, tag: str, ns: dict) -> str:
        """Get text content of a child element."""
        ns_prefix = 'm:' if ns else ''
        child = elem.find(f'{ns_prefix}{tag}', ns) if ns else elem.find(tag)
        if child is None:
            child = elem.find(tag)
        return child.text.strip() if child is not None and child.text else ''
    
    def _make_key(self, group_id: str, artifact_id: str) -> str:
        """Create a lookup key for a dependency."""
        return f"{group_id}:{artifact_id}".lower()
    
    def analyze(self, component: Component) -> ExclusionInfo:
        """Check if a component is excluded."""
        result = ExclusionInfo()
        
        keys_to_check = [
            self._make_key(component.group_id, component.name),
            self._make_key('', component.name),
            f":{component.name}".lower(),
        ]
        
        if component.group_id:
            keys_to_check.append(f"{component.group_id}:*".lower())
        
        for key in keys_to_check:
            if key in self.exclusions:
                result.is_excluded = True
                for excl in self.exclusions[key]:
                    result.excluded_in_files.append(excl['pom_file'])
                    result.excluded_by.append(excl['excluded_from'])
                result.exclusion_scope = "transitive"
                break
            
            for excl_key, excl_data in self.exclusions.items():
                if component.name.lower() in excl_key:
                    result.is_excluded = True
                    for excl in excl_data:
                        result.excluded_in_files.append(excl['pom_file'])
                        result.excluded_by.append(excl['excluded_from'])
                    result.exclusion_scope = "transitive"
                    break
        
        for key in keys_to_check:
            if key in self.dependency_management:
                mgmt = self.dependency_management[key]
                if mgmt['scope'] in ('provided', 'test'):
                    if not result.is_excluded:
                        result.exclusion_scope = "scoped"
                        result.excluded_in_files.append(mgmt['pom_file'])
                        result.excluded_by.append(f"scope:{mgmt['scope']}")
        
        result.excluded_in_files = list(set(result.excluded_in_files))[:5]
        result.excluded_by = list(set(result.excluded_by))[:5]
        
        return result


